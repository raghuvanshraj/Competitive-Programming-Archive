struct euclid_t {
	int x;
	int y;
	int g;
};

euclid_t extended_euclid(int a, int b) {
	euclid_t ans({
		1, 0, a
	});

	int x1 = 0, y1 = 1, b1 = b;
	while (b1) {
		int q = ans.g / b1;
		tie(ans.x, x1) = make_tuple(x1, ans.x - q * x1);
		tie(ans.y, y1) = make_tuple(y1, ans.y - q * y1);
		tie(ans.g, b1) = make_tuple(b1, ans.g - q * b1);
	}

	return ans;
}

int main(int argc, char const *argv[]) {
	ios::sync_with_stdio(false);
	cin.tie(0);

	int a, b;
	while ((cin >> a) and (cin >> b)) {
		euclid_t ans = extended_euclid(a, b);
		cout << ans.x << ' ' << ans.y << ' ' << ans.g << endl;
	}

	return 0;
}