int main(int argc, char const *argv[]) {
	ios::sync_with_stdio(false);
	cin.tie(0);

	string s;
	cin >> s;
	int n = s.size();
	int i = 0, j = 0;
	vector<int> masks;
	for (int i = 0; i <= n - 1; ++i) {
		int curr_mask = 0;
		for (int j = 0; i + j <= n - 1; ++j) {
			if (curr_mask & (1 << (s[i + j] - 'a'))) {
				break;
			}
			curr_mask |= (1 << (s[i + j] - 'a'));
			masks.push_back(curr_mask);
		}
	}

	int max_size = (1 << 20);
	vector<int> dp(max_size);
	for (int mask : masks) {
		if (dp[mask]) {
			continue;
		}

		for (int j = 0; j <= 19; ++j) {
			if (mask & (1 << j)) {
				dp[mask]++;
			}
		}
	}

	for (int i = 1; i <= max_size - 1; ++i) {
		for (int j = 0; j <= 19; ++j) {
			if (i & (1 << j)) {
				dp[i] = max(dp[i], dp[i ^ (1 << j)]);
			}
		}
	}

	int ans = 0;
	for (int mask : masks) {
		int curr_count = 0;
		for (int j = 0; j <= 19; ++j) {
			if (mask & (1 << j)) {
				curr_count++;
			}
		}

		ans = max(ans, curr_count + dp[mask ^ (max_size - 1)]);
	}

	cout << ans;

	return 0;
}