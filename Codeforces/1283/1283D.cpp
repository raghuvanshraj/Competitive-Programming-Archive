template <
    typename T,
    typename = typename enable_if<is_arithmetic<T>::value, T>::type,
    typename U,
    typename = typename enable_if<is_arithmetic<T>::value, T>::type
    >
pair<T, U> operator+(const pair<T, U> &a, const pair<T, U> &b) {
	return {a.first + b.first, a.second + b.second};
}

template <
    typename T,
    typename = typename enable_if<is_arithmetic<T>::value, T>::type,
    typename U,
    typename = typename enable_if<is_arithmetic<T>::value, T>::type
    >
pair<T, U> operator-(const pair<T, U> &a, const pair<T, U> &b) {
	return {a.first - b.first, a.second - b.second};
}

void christmas_trees(int trees[], int n, int m) {
	vector<int> ans;
	queue<int> qu;
	map<int, int> dist;
	for (int i = 0; i < n; ++i) {
		qu.push(trees[i]);
		dist[trees[i]] = 0;
	}

	ll total = 0;
	while (not qu.empty()) {
		if (ans.size() == m) {
			break;
		}

		int curr = qu.front();
		qu.pop();
		if (dist[curr] != 0) {
			ans.push_back(curr);
			total += (ll)dist[curr];
		}

		if (dist.find(curr - 1) == dist.end()) {
			dist[curr - 1] = dist[curr] + 1;
			qu.push(curr - 1);
		}
		if (dist.find(curr + 1) == dist.end()) {
			dist[curr + 1] = dist[curr] + 1;
			qu.push(curr + 1);
		}
	}

	cout << total << endl;
	print_arr(ans, m);
}

int main(int argc, char const *argv[]) {
	ios::sync_with_stdio(false);
	cin.tie(0);

	int n, m;
	cin >> n >> m;
	int trees[n];
	input_arr(trees, n);

	christmas_trees(trees, n, m);

	return 0;
}