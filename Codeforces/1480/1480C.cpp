vector<int> arr;

int query(int i) {
	if (arr[i] != 0) {
		return arr[i];
	}
	cout << "? " << i + 1 << endl;
	int x;
	cin >> x;
	return arr[i] = x;
}

void print(int x) {
	cout << "! " << x + 1 << endl;
}

int main(int argc, char const *argv[]) {
	ios::sync_with_stdio(false);
	cin.tie(0);

	int n;
	cin >> n;
	int l = 0, r = n - 1;
	int cnt = 100;
	arr = vector<int>(n);
	while (cnt--) {
		if (l == r) {
			print(l);
			break;
		} else if (l == r - 1) {
			int x = query(l);
			int y = query(r);
			if (x < y) {
				print(l);
			} else {
				print(r);
			}
			break;
		}

		int left_diff = query(l + 1) - query(l);
		if (left_diff > 0) {
			print(l);
			break;
		}
		int right_diff = query(r) - query(r - 1);
		if (right_diff < 0) {
			print(r);
			break;
		}

		int mid = (l + r) / 2;
		int mid_left_diff = query(mid) - query(mid - 1);
		int mid_right_diff = query(mid + 1) - query(mid);
		if (mid_right_diff > 0 and mid_left_diff < 0) {
			print(mid);
			break;
		} else {
			if (mid_left_diff > 0) {
				r = mid - 1;
			} else if (mid_right_diff < 0) {
				l = mid + 1;
			}
		}
	}

	return 0;
}