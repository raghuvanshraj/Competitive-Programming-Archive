int main(int argc, char const *argv[]) {
	ios::sync_with_stdio(false);
	cin.tie(0);

	map<int, string> mp = {
		{0, "1869"},
		{1, "1968"},
		{2, "1689"},
		{3, "6891"},
		{4, "1698"},
		{5, "1986"},
		{6, "1896"}
	};

	string s;
	cin >> s;
	map<char, int> counts;
	for (char c : s) {
		counts[c]++;
	}

	int n = s.size();
	string ans(n, '0');
	int j = 0;
	int mod = 0;
	for (int i = 0; i <= n - 1; ++i) {
		if (s[i] == '1' or
		        s[i] == '6' or
		        s[i] == '8' or
		        s[i] == '9') {

			if (counts[s[i]] > 1) {
				ans[j++] = s[i];
				mod = (mod * 10 + s[i] - '0') % 7;
				counts[s[i]]--;
			}
		} else if (s[i] != '0') {
			ans[j++] = s[i];
			mod = (mod * 10 + s[i] - '0') % 7;
			counts[s[i]]--;
		}
	}

	mod = (mod * 10000) % 7;

	string curr = mp[(7 - mod) % 7];
	for (char c : curr) {
		ans[j++] = c;
	}

	cout << ans;

	return 0;
}