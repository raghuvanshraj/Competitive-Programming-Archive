int main(int argc, char const *argv[]) {
	ios::sync_with_stdio(false);
	cin.tie(0);

	string s1, s2;
	cin >> s1 >> s2;
	int n = s1.size(), m = s2.size();
	vector<int> f(n, -1), g(n, m);
	if (s1[0] == s2[0]) {
		f[0] = 0;
	}
	if (s1[n - 1] == s2[m - 1]) {
		g[n - 1] = m - 1;
	}

	for (int i = 1; i <= n - 1; ++i) {
		f[i] = f[i - 1];
		int idx = f[i];
		if (idx < m - 1) {
			if (s1[i] == s2[idx + 1]) {
				f[i]++;
			}
		}
	}

	for (int i = n - 2; i >= 0; --i) {
		g[i] = g[i + 1];
		int idx = g[i];
		if (idx > 0) {
			if (s1[i] == s2[idx - 1]) {
				g[i]--;
			}
		}
	}

	int ans = 0;
	for (int i = 0; i <= n - 1; ++i) {
		int idx = (i < n - 1) ? g[i + 1] : m;
		if (idx == 0) {
			ans = max(ans, i + 1);
			continue;
		}

		int prev_idx = lower_bound(ALL(f), idx - 1) - f.begin();
		if (prev_idx < i) {
			ans = max(ans, i - prev_idx);
		}
	}

	cout << ans;

	return 0;
}